// Write a program that prints the numbers from 1 to 10, one per line.
// Input: There is no input for this problem.
// Output: Your program should output the numbers from 1 to 10, each on a separate line.

#include <stdio.h>

int main()
{
    int i;
    for (i = 1; i <= 10; i++)
    {
        printf("%d\n", i);
    }
    return 0;
}
